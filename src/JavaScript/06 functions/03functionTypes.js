//?function types

//* function declaration
function subtract(a, b) {
   return a - b
}

//* function expression
const subtractExpression = function (a, b) {
   return a - b
}

//* arrow function
const subtractArrow = (a, b) => {
   return a - b
}

//* arrow function concise
const subtractArrow1 = (a, b) => a - b
